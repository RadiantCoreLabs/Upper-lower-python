## Laver alle små bogstaver om til stor bogstaver, her bruges .upper()

first_name = "thomas"

last_name = "jensen"

NameCourse = "python"

print(first_name.upper())

print(last_name.upper())

print(NameCourse.upper())

## Laver alle stor bogstaver om til små bogstaver, her bruges .lower()

first_name = "THOMAS"

last_name = "JENSEN"

NameCourse = "PYTHON"

print(first_name.lower())

print(last_name.lower())

print(NameCourse.lower())